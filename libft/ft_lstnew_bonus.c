#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*ret;

	ret = malloc(sizeof(t_list));
	if (ret != NULL)
	{
		ret->content = (void *)content;
		ret->next = NULL;
	}
	return (ret);
}
